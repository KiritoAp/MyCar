    private void btProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarActionPerformed
        ViaCEP viacep = new ViaCEP();
        try {
            viacep.buscar(jftCEP.getText());
            edEstado.setText(viacep.getUf());
            edCidade.setText(viacep.getLocalidade());
            edBairro.setText(viacep.getBairro());
            edEndereco.setText(viacep.getLogradouro());
        } catch (ViaCEPException ex) {
            Logger.getLogger(fTelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btProcurarActionPerformed

