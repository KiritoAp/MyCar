    private void btCadastrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarUsuarioActionPerformed

        
        Usuario u = new Usuario();
        UsuarioDao dao = new UsuarioDao();
        u.setLoginUser(edLoginUsuario.getText());
        u.setPasswordUser(edSenhaUsuario.getText());
        u.setNameUser(edNomeCompleto.getText());
        u.setDateUser(jftDatadeNascimento.getText());
        u.setGenderUser(cbGenderUser.getSelectedIndex());
        u.setCpfUser(jftCPF.getText());
        u.setPhoneUser(jftTelefone.getText());
        u.setEmailUser(edEmailUsuario.getText());
        u.setCepUser(jftCEP.getText());
        u.setCountryUser(edEstado.getText());
        u.setCityUser(edCidade.getText());
        u.setDistrictUser(edBairro.getText());
        u.setAddressUser(edEndereco.getText());
        u.setNumberHouseUser(edNumero.getText());
        u.setComplementHouseUser(edComplemento.getText());
        dao.create(u);
        FTelaInicial TelaInicial = new FTelaInicial();
        TelaInicial.setVisible(true);
        dispose();
                
         
   

    }//GEN-LAST:event_btCadastrarUsuarioActionPerformed

